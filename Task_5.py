# Задача 5
# Устройство состоит из трёх деталей. Нужно найти вероятность того, что выйдут из строя: а) все детали, б) только две детали, в) хотя бы одна деталь, г) от одной до двух деталей.

# Вероятности отказа каждой детали
P_A = 0.1  # Для первой детали
P_B = 0.2  # Для второй детали
P_C = 0.25  # Для третьей детали

# а) Вероятность, что выйдут из строя все детали
prob_all_fail = P_A * P_B * P_C

# б) Вероятность, что выйдут из строя только две детали
prob_two_fail = (
    P_A * P_B * (1 - P_C) + 
    P_A * (1 - P_B) * P_C + 
    (1 - P_A) * P_B * P_C
)

# в) Вероятность, что выйдет из строя хотя бы одна деталь
prob_at_least_one_fail = 1 - (1 - P_A) * (1 - P_B) * (1 - P_C)

# г) Вероятность, что выйдут из строя от одной до двух деталей
prob_one_to_two_fail = prob_two_fail + (P_A * (1 - P_B) * (1 - P_C) +
                                        (1 - P_A) * P_B * (1 - P_C) +
                                        (1 - P_A) * (1 - P_B) * P_C)

print(f"Вероятность, что выйдут из строя все детали: {prob_all_fail:.6f}")
print(f"Вероятность, что выйдут из строя только две детали: {prob_two_fail:.6f}")
print(f"Вероятность, что выйдет из строя хотя бы одна деталь: {prob_at_least_one_fail:.6f}")
print(f"Вероятность, что выйдут из строя от одной до двух деталей: {prob_one_to_two_fail:.6f}")
